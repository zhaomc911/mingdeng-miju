export function parseBoard(layout: string[]): Board {
  return layout.map((row) =>
    row.split('').map((char) => {
      if (char === '.') {
        return { kind: 'white' } as const;
      }
      if (char === '#') {
        return { kind: 'black' } as const;
      }
      return { kind: 'black', clue: Number(char) as 0 | 1 | 2 | 3 | 4 } as const;
    })
  );
}

export const LEVELS: Level[] = LEVEL_LAYOUTS.map((level) => ({
  ...level,
  board: parseBoard(level.layout),
}));
